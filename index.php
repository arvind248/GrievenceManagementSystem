        Grievance Handling procedure University Level
            1.	When ever a complaints/grievance is received from a student it will be recorded and a unique 
            number will be given to the grievance for future reference. (GCFC/DEPT/A.Y/XXXX)
            2.	Recorded grievance will be forwarded to the concerned department/ Committee/Person asking them 
            about the information and a timeframe required to provide a solution
            3.	Acknowledge the student/parent with the information given by the concerned department/
            committee/person.
            4.	Enquire the status of grievance which is under process (periodically)
            5.	At the end of stipulated time a report/solution will be collected from concerned department/ 
            committee/person
            6.	If the grievance is resolved to the expected level of satisfaction, the student/parent is 
            supplied with the solution provided by department/ committee/person
            7.	If the grievance is not resolved to the expected level of satisfaction, a detailed report 
            stating the reasons for not arriving at the expected solution will be collected
            8.	If the reason for not resolving the grievance is insufficient time, the time required will be 
            re scheduled appropriately by looking in to the details and same will be acknowledged to the 
            student/parent (repeat the steps 4,5,6).
            9.	Escalate the grievances/ complaints to higher management in case, the complaints and 
            grievances could not be handled by the department/ committee/person


        </pre>
