        <h3>Student Grievances</h3>
        <p>K L Deemed to be University provides an effective mechanism to promptly resolve all student grievances. The Grievance Redressal Cell was constituted by the university as per the regulations of University Grants Commission. A Grievance is any discontent or dissatisfaction, whether expressed or not, whether valid or not, arising out of anything that is directly connected to the institute, and which a student thinks, believes, or even feels, is unfair, unjust or inequitable.</p>
        <p>All grievances of the student are redressed through a single window system.  Any student with a genuine grievance may approach Associate Dean - Grievances to submit his/her grievance in a proper format. All grievances are redressed in a systematic way by involving the respective department/person directly connected to the specific issue.</p>

